package com.hj.app.mobile.merchant.card.po;

import java.io.Serializable;

public class CardPo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String openid; // openid
	private String slogan; // 标语
	private String contact_name; // 联系人
	private String contact_tel; // 联系人手机号
	private String mer_name; // 商户名称
	private String mer_addr; // 商户地址
	private String mer_goods; // 商户商品
	private String addDate; // 添加日期
	
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	
	public String getSlogan() {
		return slogan;
	}
	public void setSlogan(String slogan) {
		this.slogan = slogan;
	}
	public String getContact_name() {
		return contact_name;
	}
	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}
	public String getContact_tel() {
		return contact_tel;
	}
	public void setContact_tel(String contact_tel) {
		this.contact_tel = contact_tel;
	}
	public String getMer_name() {
		return mer_name;
	}
	public void setMer_name(String mer_name) {
		this.mer_name = mer_name;
	}
	public String getMer_addr() {
		return mer_addr;
	}
	public void setMer_addr(String mer_addr) {
		this.mer_addr = mer_addr;
	}
	public String getMer_goods() {
		return mer_goods;
	}
	public void setMer_goods(String mer_goods) {
		this.mer_goods = mer_goods;
	}
	public String getAddDate() {
		return addDate;
	}
	public void setAddDate(String addDate) {
		this.addDate = addDate;
	}
	
	
}
